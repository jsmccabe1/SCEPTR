# 6. Concordance Analysis (ExPlot vs GO Enrichment)
# ---------------------------------------------------------------------------

def concordance_analysis(explot_results_file, go_results_file, prefix):
    """Compare ExPlot keyword enrichment with topGO results."""
    if not explot_results_file or not os.path.exists(explot_results_file):
        return None, None
    if not go_results_file or not os.path.exists(go_results_file):
        return None, None

    try:
        go_df = pd.read_csv(go_results_file, sep='\t')
        with open(explot_results_file) as f:
            explot_data = json.load(f)
    except Exception as e:
        logger.warning(f"Could not load concordance data: {e}")
        return None, None

    # Extract significant GO terms per tier
    go_sig = {}
    if 'Term' in go_df.columns and 'Tier' in go_df.columns:
        for tier in go_df['Tier'].unique():
            tier_data = go_df[(go_df['Tier'] == tier) & (go_df['Fold_Enrichment'] >= 1.5)]
            if 'FDR' in go_df.columns:
                tier_data = tier_data[tier_data['FDR'] <= 0.05]
            go_sig[f"top_{tier}"] = set(tier_data['Term'].str.lower())

    # Extract significant ExPlot categories per tier
    explot_sig = {}
    for tier_name, tier_data in explot_data.items():
        if isinstance(tier_data, dict) and 'enrichment' in tier_data:
            sig_cats = set()
            for cat, stats in tier_data['enrichment'].items():
                if stats.get('significant', False) and stats.get('fold_change', 0) >= 1.5:
                    sig_cats.add(cat.lower())
            explot_sig[tier_name] = sig_cats

    summary = {
        'go_tiers_analysed': len(go_sig),
        'explot_tiers_analysed': len(explot_sig),
        'go_total_significant': sum(len(v) for v in go_sig.values()),
        'explot_total_significant': sum(len(v) for v in explot_sig.values()),
    }

    return summary, None


