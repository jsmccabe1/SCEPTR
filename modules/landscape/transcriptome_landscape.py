def calculate_gini(values):
    """Calculate Gini coefficient for expression concentration."""
    values = np.sort(np.array(values, dtype=float))
    n = len(values)
    if n == 0 or values.sum() == 0:
        return 0.0
    index = np.arange(1, n + 1)
    return (2 * np.sum(index * values) - (n + 1) * np.sum(values)) / (n * np.sum(values))


def expression_distribution_analysis(df, prefix):
    """Analyse expression distribution and generate Lorenz curve."""
    tpm = df['TPM'].values.copy()
    tpm = np.sort(tpm)
    total = tpm.sum()

    if total == 0:
        return {'gini': 0, 'top10_pct': 0, 'top50_pct': 0, 'top100_pct': 0,
                'median_tpm': 0, 'mean_tpm': 0, 'zero_pct': 0}, None

    n = len(tpm)
    cumulative = np.cumsum(tpm) / total
    x = np.arange(1, n + 1) / n

    gini = calculate_gini(tpm)

    # Top-gene concentration
    tpm_desc = np.sort(tpm)[::-1]
    cum_desc = np.cumsum(tpm_desc) / total
    top10_pct = cum_desc[min(9, n - 1)] * 100
    top50_pct = cum_desc[min(49, n - 1)] * 100
    top100_pct = cum_desc[min(99, n - 1)] * 100

    stats = {
        'gini': round(gini, 3),
        'top10_pct': round(top10_pct, 1),
        'top50_pct': round(top50_pct, 1),
        'top100_pct': round(top100_pct, 1),
        'median_tpm': round(float(np.median(tpm)), 2),
        'mean_tpm': round(float(np.mean(tpm)), 2),
        'zero_pct': round((tpm == 0).sum() / n * 100, 1),
        'total_genes': n,
        'expressed_genes': int((tpm > 0).sum()),
    }

    # --- Figure: Lorenz curve + TPM distribution ---
    fig, axes = plt.subplots(1, 2, figsize=(14, 5.5))

    # Lorenz curve
    ax1 = axes[0]
    ax1.fill_between(x, cumulative, alpha=0.15, color='#2d5a87')
    ax1.plot(x, cumulative, color='#2d5a87', linewidth=2, label=f'Expression (Gini = {gini:.3f})')
    ax1.plot([0, 1], [0, 1], '--', color='#999', linewidth=1, label='Perfect equality')
    ax1.set_xlabel('Cumulative fraction of genes (ranked by TPM)', fontsize=11)
    ax1.set_ylabel('Cumulative fraction of total expression', fontsize=11)
    ax1.set_title('Expression Concentration (Lorenz Curve)', fontsize=13, fontweight='bold')
    ax1.legend(fontsize=9, loc='upper left')
    ax1.set_xlim(0, 1)
    ax1.set_ylim(0, 1)

    # Annotate key percentiles
    for pct, label in [(10, 'Top 10'), (50, 'Top 50'), (100, 'Top 100')]:
        if pct <= n:
            gene_frac = pct / n
            expr_frac = cum_desc[pct - 1]
            ax1.annotate(f'{label}: {expr_frac * 100:.0f}%',
                         xy=(1 - gene_frac, 1 - (1 - expr_frac)),
                         fontsize=8, color='#e8913a', fontweight='bold')

    # TPM distribution (log scale)
    ax2 = axes[1]
    tpm_nonzero = tpm[tpm > 0]
    if len(tpm_nonzero) > 0:
        log_tpm = np.log10(tpm_nonzero)
        ax2.hist(log_tpm, bins=50, color='#2d5a87', alpha=0.7, edgecolor='white', linewidth=0.5)
        ax2.axvline(np.log10(np.median(tpm_nonzero)), color='#e8913a', linestyle='--',
                    linewidth=2, label=f'Median: {np.median(tpm_nonzero):.1f} TPM')
        ax2.set_xlabel('log₁₀(TPM)', fontsize=11)
        ax2.set_ylabel('Number of genes', fontsize=11)
        ax2.set_title('Expression Distribution', fontsize=13, fontweight='bold')
        ax2.legend(fontsize=9)

    plt.tight_layout()
    b64 = save_and_encode(fig, f"{prefix}_expression_distribution.png")

    return stats, b64


