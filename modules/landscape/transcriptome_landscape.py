def fig_to_base64(fig, dpi=150):
    """Convert matplotlib figure to base64 PNG string."""
    buf = BytesIO()
    fig.savefig(buf, format='png', dpi=dpi, bbox_inches='tight', facecolor='white')
    buf.seek(0)
    b64 = base64.b64encode(buf.read()).decode('utf-8')
    plt.close(fig)
    return b64


def save_and_encode(fig, filepath, dpi=300):
    """Save figure to file AND return base64."""
    fig.savefig(filepath, dpi=dpi, bbox_inches='tight', facecolor='white')
    svg_path = filepath.replace('.png', '.svg')
    fig.savefig(svg_path, bbox_inches='tight', facecolor='white', format='svg')
    b64 = fig_to_base64(fig, dpi=150)
    return b64


