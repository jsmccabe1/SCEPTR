def annotation_quality_analysis(df, prefix):
    """Assess annotation completeness across expression levels."""
    n = len(df)
    has_uniprot = df['uniprot_id'].notna() & (df['uniprot_id'].astype(str).str.strip() != '')
    has_protein_name = False
    if 'protein_name' in df.columns:
        has_protein_name = df['protein_name'].notna() & (
            df['protein_name'].astype(str).str.strip() != '') & (
            df['protein_name'].astype(str).str.lower() != 'unknown protein')

    go_cols = ['GO_Biological_Process', 'GO_Cellular_Component', 'GO_Molecular_Function']
    has_any_go = pd.Series([False] * n, index=df.index)
    go_stats = {}
    for col in go_cols:
        if col in df.columns:
            has_col = df[col].notna() & (df[col].astype(str).str.strip() != '') & (
                df[col].astype(str).str.strip() != 'nan')
            has_any_go = has_any_go | has_col
            go_stats[col] = int(has_col.sum())

    overall = {
        'total_genes': n,
        'with_diamond_hit': int(has_uniprot.sum()),
        'diamond_pct': round(has_uniprot.sum() / n * 100, 1) if n > 0 else 0,
        'with_any_go': int(has_any_go.sum()),
        'go_pct': round(has_any_go.sum() / n * 100, 1) if n > 0 else 0,
        'go_by_category': go_stats,
    }

    if isinstance(has_protein_name, pd.Series):
        overall['with_protein_name'] = int(has_protein_name.sum())

    # Annotation rate by expression decile
    df_sorted = df.sort_values('TPM', ascending=False).reset_index(drop=True)
    n_per_decile = max(1, n // 10)
    decile_stats = []
    for i in range(10):
        start = i * n_per_decile
        end = min((i + 1) * n_per_decile, n)
        subset = df_sorted.iloc[start:end]
        n_sub = len(subset)
        if n_sub == 0:
            continue

        sub_uniprot = subset['uniprot_id'].notna() & (subset['uniprot_id'].astype(str).str.strip() != '')
        sub_go = pd.Series([False] * n_sub, index=subset.index)
        for col in go_cols:
            if col in subset.columns:
                sub_go = sub_go | (subset[col].notna() & (subset[col].astype(str).str.strip() != '') &
                                   (subset[col].astype(str).str.strip() != 'nan'))

        decile_stats.append({
            'decile': i + 1,
            'label': f"{'Top' if i == 0 else ''} {(i)*10+1}-{(i+1)*10}%",
            'n_genes': n_sub,
            'diamond_pct': round(sub_uniprot.sum() / n_sub * 100, 1),
            'go_pct': round(sub_go.sum() / n_sub * 100, 1),
            'median_tpm': round(float(subset['TPM'].median()), 2),
        })

    # --- Figure: annotation rate by expression level ---
    if decile_stats:
        fig, ax = plt.subplots(figsize=(10, 5))
        deciles = [d['decile'] for d in decile_stats]
        diamond_rates = [d['diamond_pct'] for d in decile_stats]
        go_rates = [d['go_pct'] for d in decile_stats]

        x = np.arange(len(deciles))
        width = 0.35
        ax.bar(x - width / 2, diamond_rates, width, label='DIAMOND hit', color='#2d5a87', alpha=0.8)
        ax.bar(x + width / 2, go_rates, width, label='GO annotation', color='#e8913a', alpha=0.8)
        ax.set_xlabel('Expression decile (1 = highest)', fontsize=11)
        ax.set_ylabel('Annotation rate (%)', fontsize=11)
        ax.set_title('Annotation Completeness by Expression Level', fontsize=13, fontweight='bold')
        ax.set_xticks(x)
        ax.set_xticklabels([f"D{d}" for d in deciles], fontsize=9)
        ax.legend(fontsize=10)
        ax.set_ylim(0, 105)
        ax.spines['top'].set_visible(False)
        ax.spines['right'].set_visible(False)
        ax.grid(axis='y', alpha=0.3)

        plt.tight_layout()
        b64 = save_and_encode(fig, f"{prefix}_annotation_quality.png")
    else:
        b64 = None

    return overall, decile_stats, b64


