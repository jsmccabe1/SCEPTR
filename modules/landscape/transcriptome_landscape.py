def main():
    parser = argparse.ArgumentParser(description='SCEPTR Transcriptome Landscape Analysis')
    parser.add_argument('integrated_results', help='Merged annotation+expression TSV')
    parser.add_argument('--prefix', default='sceptr', help='Output file prefix')
    parser.add_argument('--categories', default=None,
                        help='Functional categories JSON (for shift analysis)')
    parser.add_argument('--explot_results', default=None,
                        help='ExPlot results JSON (for concordance)')
    parser.add_argument('--go_results', default=None,
                        help='GO enrichment results TSV (for concordance)')
    parser.add_argument('--top_genes', type=int, default=30,
                        help='Number of genes in dominance table')
    parser.add_argument('--top_taxa', type=int, default=20,
                        help='Number of taxa to show')
    parser.add_argument('--outdir', default='.',
                        help='Output directory (default: current directory)')
    args = parser.parse_args()

    logger.info("SCEPTR Transcriptome Landscape Analysis")
    logger.info(f"  Input: {args.integrated_results}")

    # Set up output directory structure
    fig_dir = os.path.join(args.outdir, 'figures')
    os.makedirs(fig_dir, exist_ok=True)
    fig_prefix = os.path.join(fig_dir, args.prefix)
    data_prefix = os.path.join(args.outdir, args.prefix)

    # Load data
    try:
        df = pd.read_csv(args.integrated_results, sep='\t')
    except Exception:
        df = pd.read_csv(args.integrated_results, sep=',')
    logger.info(f"Loaded {len(df)} genes")

    if 'TPM' not in df.columns:
        candidates = [c for c in df.columns if c.lower() in ['tpm', 'expression', 'fpkm']]
        if candidates:
            df['TPM'] = df[candidates[0]]
        else:
            logger.error("No TPM column found")
            sys.exit(1)

    df = df.sort_values('TPM', ascending=False).reset_index(drop=True)

    # 1. Expression distribution
    logger.info("Analysing expression distribution...")
    expr_stats, expr_fig = expression_distribution_analysis(df, fig_prefix)
    logger.info(f"  Gini: {expr_stats['gini']}, Top 10: {expr_stats['top10_pct']}%")

    # 2. Dominance table
    logger.info("Building dominance table...")
    dominance_df = build_dominance_table(df, args.top_genes)

    # 3. Annotation quality
    logger.info("Assessing annotation quality...")
    ann_stats, ann_deciles, ann_fig = annotation_quality_analysis(df, fig_prefix)
    logger.info(f"  DIAMOND: {ann_stats['diamond_pct']}%, GO: {ann_stats['go_pct']}%")

    # 4. Taxonomic distribution
    logger.info("Analysing taxonomic distribution...")
    tax_stats, tax_fig = taxonomic_distribution(df, fig_prefix, args.top_taxa)
    if tax_stats:
        logger.info(f"  Top: {tax_stats.get('top_organism', 'N/A')} ({tax_stats.get('top_organism_pct', 0)}%)")

    # 5. Functional shifts
    shift_fig = None
    categories = None
    if args.categories:
        try:
            with open(args.categories) as f:
                categories = json.load(f)
            logger.info(f"Loaded {len(categories)} categories for shift analysis")
            shift_fig = functional_shift_analysis(df, categories, fig_prefix)
        except Exception as e:
            logger.warning(f"Could not load categories: {e}")

    # 6. Concordance
    concordance_stats = None
    if args.explot_results and args.go_results:
        logger.info("Computing ExPlot/GO concordance...")
        concordance_stats, _ = concordance_analysis(args.explot_results, args.go_results, fig_prefix)

    # Generate report
    logger.info("Generating landscape report...")
    report = generate_landscape_report(
        expr_stats, expr_fig,
        dominance_df,
        ann_stats, ann_deciles, ann_fig,
        tax_stats, tax_fig,
        shift_fig,
        concordance_stats,
        data_prefix, len(df))

    # Save stats as JSON
    stats_out = {
        'expression': expr_stats,
        'annotation': ann_stats,
        'taxonomy': tax_stats,
    }
    with open(f"{data_prefix}_landscape_stats.json", 'w') as f:
        json.dump(stats_out, f, indent=2, default=str)

    logger.info("Landscape analysis complete!")


if __name__ == "__main__":
    main()
