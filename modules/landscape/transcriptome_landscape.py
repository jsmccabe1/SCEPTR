def build_dominance_table(df, top_n=30):
    """Build top-N most expressed genes with cumulative percentage."""
    total_tpm = df['TPM'].sum()
    if total_tpm == 0:
        return pd.DataFrame()

    top = df.nlargest(top_n, 'TPM').copy()
    top['cumulative_tpm'] = top['TPM'].cumsum()
    top['pct_of_total'] = (top['TPM'] / total_tpm * 100).round(2)
    top['cumulative_pct'] = (top['cumulative_tpm'] / total_tpm * 100).round(2)
    top['rank'] = range(1, len(top) + 1)

    cols = ['rank', 'sequence_id', 'TPM', 'pct_of_total', 'cumulative_pct']
    for c in ['protein_name', 'gene_names', 'organism', 'uniprot_id']:
        if c in top.columns:
            cols.insert(3, c)

    return top[[c for c in cols if c in top.columns]]


