def _normalize_landscape_categories(categories):
    """Extract keyword lists from either legacy or v2 category format."""
    normalised = {}
    for cat, val in categories.items():
        if isinstance(val, dict) and 'keywords' in val:
            normalised[cat] = val['keywords']
        elif isinstance(val, list):
            normalised[cat] = val
        else:
            normalised[cat] = []
    return normalised


def functional_shift_analysis(df, categories, prefix):
    """Show how functional composition changes across expression rank."""
    import re

    if not categories:
        return None

    # Normalise v2 format to plain keyword lists
    categories = _normalize_landscape_categories(categories)

    df_sorted = df.sort_values('TPM', ascending=False).reset_index(drop=True)
    n = len(df_sorted)

    # Identify annotation fields
    ann_fields = [c for c in df.columns if c in [
        'protein_name', 'Protein_Name', 'GO_Biological_Process',
        'GO_Molecular_Function', 'GO_Cellular_Component',
        'annotation', 'description', 'product']]

    if not ann_fields:
        return None

    # Sliding window: 200-gene windows, step 50
    window_size = min(200, n // 5) if n > 100 else n
    step = max(window_size // 4, 1)

    window_centres = []
    category_fractions = {cat: [] for cat in categories}

    for start in range(0, n - window_size + 1, step):
        end = start + window_size
        window = df_sorted.iloc[start:end]
        centre_rank = (start + end) / 2
        window_centres.append(centre_rank)

        # Count category matches in this window
        window_text = []
        for _, row in window.iterrows():
            parts = []
            for f in ann_fields:
                v = row.get(f)
                if pd.notna(v):
                    parts.append(str(v).lower())
            window_text.append(' '.join(parts))

        for cat, keywords in categories.items():
            match_count = 0
            for text in window_text:
                for kw in keywords:
                    if re.search(r'\b' + re.escape(kw.lower()) + r'\b', text):
                        match_count += 1
                        break
            category_fractions[cat].append(match_count / window_size * 100)

    if not window_centres:
        return None

    # --- Figure: stacked area chart ---
    fig, ax = plt.subplots(figsize=(14, 6.5))

    # Use a qualitative colormap
    n_cats = len(categories)
    cmap = plt.cm.get_cmap('tab20', n_cats)
    colors = [cmap(i) for i in range(n_cats)]

    cat_names = list(categories.keys())
    # Remove Uncharacterised from stacking (plot separately)
    main_cats = [c for c in cat_names if 'uncharacteri' not in c.lower()]
    unchar_cat = [c for c in cat_names if 'uncharacteri' in c.lower()]

    y_stack = np.array([category_fractions[c] for c in main_cats])
    ax.stackplot(window_centres, y_stack, labels=main_cats, colors=colors[:len(main_cats)], alpha=0.8)

    # Overlay uncharacterised as a dashed line
    for uc in unchar_cat:
        ax.plot(window_centres, category_fractions[uc], '--', color='#999',
                linewidth=2, label=uc)

    ax.set_xlabel('Expression rank (1 = highest)', fontsize=11)
    ax.set_ylabel('Functional composition (% of window)', fontsize=11)
    ax.set_title('Functional Composition Across Expression Gradient', fontsize=13, fontweight='bold')
    ax.legend(loc='center left', bbox_to_anchor=(1.02, 0.5), fontsize=8, frameon=False)
    ax.set_xlim(window_centres[0], window_centres[-1])
    ax.spines['top'].set_visible(False)
    ax.spines['right'].set_visible(False)

    plt.tight_layout()
    b64 = save_and_encode(fig, f"{prefix}_functional_shifts.png")

    return b64


